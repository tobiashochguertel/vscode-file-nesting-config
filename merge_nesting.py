#!/usr/bin/env -S uv run --script
# /// script
# requires-python = ">=3.11"
# dependencies = [
#   "httpx>=0.27",
#   "pydantic>=2.7",
#   "rich>=13",
#   "json5>=0.9",
#   "PyYAML>=6",
